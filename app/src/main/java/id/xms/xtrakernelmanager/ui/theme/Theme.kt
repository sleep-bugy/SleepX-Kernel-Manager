@Composable
fun XtraTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // Enable edge-to-edge with transparent system bars
            window.statusBarColor = android.graphics.Color.TRANSPARENT
            window.navigationBarColor = android.graphics.Color.TRANSPARENT

            val windowInsetsController = WindowCompat.getInsetsController(window, view)
            windowInsetsController.isAppearanceLightStatusBars = !darkTheme
            windowInsetsController.isAppearanceLightNavigationBars = !darkTheme

            // Enable blur behind for Android 12+ (simulating Android 16 behavior)
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) {
                window.setFlags(
                    android.view.WindowManager.LayoutParams.FLAG_BLUR_BEHIND,
                    android.view.WindowManager.LayoutParams.FLAG_BLUR_BEHIND
                )
            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography(),
        shapes = Shapes(
            // Material Expressive uses more rounded corners
            small = RoundedCornerShape(28.dp),
            medium = RoundedCornerShape(28.dp),
            large = RoundedCornerShape(28.dp)
        ),
        content = content
    )
}